/**
 * Нормализует входящий текст для надежного поиска ключевых слов
 * - Приводит к нижнему регистру
 * - Убирает лишние пробелы
 * - (Опционально) можно добавить удаление знаков препинания
 */
export const normalizeText = (text) => {
  if (!text) return '';
  return text
    .toLowerCase()
    .trim()
    .replace(/\s+/g, ' '); // Заменяет множественные пробелы на один
};